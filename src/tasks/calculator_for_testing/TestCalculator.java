package homework.calculator_for_testing;

public class TestCalculator {
    public int sum(int v1, int v2) {
        return v1 + v2;
    }

    public int sub(int v1,int v2){
        return v1-v2;
    }

    public int div(int v1,int v2){
        return v1/v2;
    }
    public int mult(int v1,int v2){
        return v1*v2;
    }
}
